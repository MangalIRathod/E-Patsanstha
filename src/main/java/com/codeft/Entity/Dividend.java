package com.codeft.Entity;

public class Dividend {
	
	 String accountnumber;
	 String fullname;
	 String share_total;
	 String monthly_deposit_total;	 
	 String share_dividend;
	 String monthly_deposit_dividend;
	 String total_dividend;
	 
	 
	public String getAccountnumber() {
		return accountnumber;
	}
	public void setAccountnumber(String accountnumber) {
		this.accountnumber = accountnumber;
	}
	public String getFullname() {
		return fullname;
	}
	public void setFullname(String fullname) {
		this.fullname = fullname;
	}
	public String getShare_total() {
		return share_total;
	}
	public void setShare_total(String share_total) {
		this.share_total = share_total;
	}
	public String getMonthly_deposit_total() {
		return monthly_deposit_total;
	}
	public void setMonthly_deposit_total(String monthly_deposit_total) {
		this.monthly_deposit_total = monthly_deposit_total;
	}
	public String getShare_dividend() {
		return share_dividend;
	}
	public void setShare_dividend(String share_dividend) {
		this.share_dividend = share_dividend;
	}
	public String getMonthly_deposit_dividend() {
		return monthly_deposit_dividend;
	}
	public void setMonthly_deposit_dividend(String monthly_deposit_dividend) {
		this.monthly_deposit_dividend = monthly_deposit_dividend;
	}
	public String getTotal_dividend() {
		return total_dividend;
	}
	public void setTotal_dividend(String total_dividend) {
		this.total_dividend = total_dividend;
	}
	 
	 

}
